/**
 * This class hold a position represented by a x-coordinate and a y-coordinate.
 * @author mira
 */
public class Position {
	/**
	 * x-coordinate of the position
	 */
	int x;
	/**
	 * y-coordinate of the position
	 */
	int y;
	
	/**
	 * Initializes the coordinates of a new position with the specified values.
	 * @param x representing the x-coordinate of the position.
	 * @param y representing the y-coordinate of the position.
	 */
	public Position(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
